export const getAllBlogIds = () => {
  return Object.keys(blogData);
};

export const getBlogById = (id) => {
  return blogData[id] || null;
};

export const getRelatedBlogs = (currentId, limit = 3) => {
  const allIds = getAllBlogIds();
  const otherIds = allIds.filter(id => id !== currentId);
  return otherIds.slice(0, limit).map(id => blogData[id]);
};
